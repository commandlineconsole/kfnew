package onlineex.Beginner

object VariablesValuesAndTypes {

  def main(args: Array[String]): Unit = {

    var x: Int = 22
    println(x)

    var y : Any = 3232.23
    println(y)

    var aa  = "hello"
    aa = "pretty"
    aa = "Jagan"
    println(aa)

    val cc: Double = 3.14
    println(cc)

    var dd: Double = 9.99
    dd = 11.001
    println(dd)

    var gg = 8.88
    println(gg)


  }
}
